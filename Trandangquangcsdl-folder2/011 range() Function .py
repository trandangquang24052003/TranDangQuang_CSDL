# Tạo một đối tượng range từ 0 đến 4 (không bao gồm 5)
x = range(5)

# In đối tượng range
print(x)          # In ra: range(0, 5)

# In kiểu dữ liệu của đối tượng range
print(type(x))    # In ra: <class 'range'>

# Truy cập và in ra phần tử đầu tiên của đối tượng range
print(x[0])       # In ra: 0
