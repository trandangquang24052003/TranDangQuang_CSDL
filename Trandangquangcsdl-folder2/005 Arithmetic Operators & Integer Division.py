x = 10
y = 3

tong = x + y  # 10 + 3 = 13
hieu = x - y  # 10 - 3 = 7
tich = x * y  # 10 * 3 = 30
thuong = x / y  # 10 / 3 = 3.3333333333333335 (là số thực)
chia_lay_so_nguyen = x // y  # 10 // 3 = 3 (là số nguyên)
chia_lay_du = x % y  # 10 % 3 = 1 (là phần dư)

print(tong)
print(hieu)
print(tich)
print(thuong)
print(chia_lay_so_nguyen)
print(chia_lay_du)
