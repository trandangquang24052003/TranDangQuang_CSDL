# Sử dụng hàm input() để nhập số a từ người dùng
a = input("Nhập số a: ")

# Sử dụng hàm input() để nhập số b từ người dùng
b = input("Nhập số b: ")

# Chuyển đổi chuỗi thành số nguyên
a = int(a)
b = int(b)

# Thực hiện phép cộng
tong = a + b

# In kết quả ra màn hình
print("Tổng của", a, "và", b, "là:", tong)
