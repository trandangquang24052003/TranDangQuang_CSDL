# Import module math và đặt tên viết tắt là m
import math as m

# In ra giá trị của hằng số π (pi) từ module math
print(m.pi)  # Kết quả: 3.141592653589793

# In ra giá trị của hằng số e từ module math
print(m.e)  # Kết quả: 2.718281828459045

# Sử dụng hàm pow() từ module math để tính 2^3 và in ra kết quả
print(m.pow(2, 3))  # Kết quả: 8.0